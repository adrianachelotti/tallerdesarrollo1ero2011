package domain.vehiculos;

/**
 * Modela un acta de infraccion
 * @author grupo 2
 *
 */
public class ActaDeInfraccion {

	String tipo;
	Integer numero;
	
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public Integer getNumero() {
		return numero;
	}
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
		
}
